/* Fundamental byte size */
#define __CHAR_BIT__ 8

/* Signed char limits */
#define __SCHAR_MIN__ (-128)
#define __SCHAR_MAX__ 127
#define __UCHAR_MAX__ 255

/* Short limits */
#define __SHRT_MIN__ (-32768)
#define __SHRT_MAX__ 32767
#define __USHRT_MAX__ 65535

/* Int limits (32-bit) */
#define __INT_MIN__ (-2147483648)
#define __INT_MAX__ 2147483647
#define __UINT_MAX__ 4294967295U

/* Long limits (64-bit) */
#define __LONG_MIN__ (-9223372036854775808L)
#define __LONG_MAX__ 9223372036854775807L
#define __ULONG_MAX__ 18446744073709551615UL

/* Long long limits (64-bit) */
#define __LLONG_MIN__ (-9223372036854775808LL)
#define __LLONG_MAX__ 9223372036854775807LL
#define __ULLONG_MAX__ 18446744073709551615ULL